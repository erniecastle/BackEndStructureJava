    public Mensaje saveDeleteDepartamentos(List<Departamentos> entitysCambios, Object[] clavesDelete, int rango, String uuidCxn) {//JSA02
        listDepartamentos = new ArrayList<Departamentos>();
        try {
            commit = true;
            inicializaVariableMensaje();
            setSession(HibernateUtil.currentSession(uuidCxn));
            getSession().beginTransaction();
            if (clavesDelete != null) {
                deleteListQuerys("Departamentos", "Clave", clavesDelete, uuidCxn);
            }
            entitysCambios = (entitysCambios == null ? new ArrayList<Departamentos>() : entitysCambios);
            if (commit && !entitysCambios.isEmpty()) {
                listDepartamentos = agregarListDepartamentos(entitysCambios, rango, uuidCxn);
            }
            if (commit) {
                mensajeResultado.setResultado(listDepartamentos);
                mensajeResultado.setNoError(0);
                mensajeResultado.setError("");
                getSession().getTransaction().commit();
            } else {
                getSession().getTransaction().rollback();
            }
        } catch (HibernateException ex) {//JSA02
            System.err.println(concatena.delete(0, concatena.length()).append(msgError).append("saveDeleteDepartamentos()1_Error: ").append(ex));
            try {
                if (getSession().getTransaction().isActive()) {
                    getSession().getTransaction().rollback();
                }
                mensajeResultado.setNoError(ControlErroresHibernate.buscaNoErrorPorExcepcion(ex.getClass()));
                mensajeResultado.setError(ex.getLocalizedMessage());
            } catch (HibernateException exc) {  //cuando marca error al hacer rollback pro ejemplo se cae conexion servidor
                mensajeResultado.setNoError(ControlErroresHibernate.buscaNoErrorPorExcepcion(exc.getClass()));
                mensajeResultado.setError(exc.getLocalizedMessage());
            }
            mensajeResultado.setResultado(null);
        }
        return mensajeResultado;
    }

    private List<Departamentos> agregarListDepartamentos(List<Departamentos> entitys, int rango, String uuidCxn) {//AAP01//JSA02
        listDepartamentos.clear();
        try {
            int i = 0;
            for (i = 0; i < entitys.size(); i++) {
                if (entitys.get(i).getId() == null) {
                    listDepartamentos.add(makePersistent(entitys.get(i)));
                } else {
                    makePersistent(entitys.get(i));
                }
                if (i % rango == 0 & i > 0) {
                    flush();
                    clear();
                }
            }
        } catch (Exception ex) {
            System.err.println(concatena.delete(0, concatena.length()).append(msgError).append("agregarListDepartamentos()1_Error: ").append(ex));
            mensajeResultado.setNoError(ControlErroresHibernate.buscaNoErrorPorExcepcion(ex.getClass()));
            mensajeResultado.setError(ex.getLocalizedMessage());
            mensajeResultado.setResultado(null);
            commit = false;
        }
        return listDepartamentos;
    }

