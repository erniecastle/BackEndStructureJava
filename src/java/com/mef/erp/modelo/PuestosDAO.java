    public Mensaje SavePuesto(List<PercepcionesFijas> agrega, Object[] eliminados, Puestos entity, String uuidCxn) {
        boolean exitoRegistro = true;
        Puestos puesto = null;
        Transaction transaction = null;
        try {
            inicializaVariableMensaje();
            Session session = HibernateUtil.currentSession(uuidCxn);
            transaction = session.beginTransaction();
            if (eliminados.length > 0) {
                exitoRegistro = deleteListQueryEn(session, "PercepcionesFijas", "id", eliminados);
            }
            if (exitoRegistro) {
                session.saveOrUpdate(entity);
                agrega = agrega == null ? new ArrayList<PercepcionesFijas>() : agrega;
                for (int i = 0; i < agrega.size(); i++) {
                    agrega.get(i).setPuestos(entity);
                    session.saveOrUpdate(agrega.get(i));
                    if (i % 100 == 0 && i > 0) {
                        session.flush();
                    }
                }
            }
            if (exitoRegistro) {
                puesto = entity;
                mensajeResultado.setResultado(puesto);
                mensajeResultado.setNoError(0);
                mensajeResultado.setError("");
                transaction.commit();
            } else {
                transaction.rollback();
            }
        } catch (HibernateException ex) {
            System.err.println(concatena.delete(0, concatena.length()).append(msgError).append("SavePuesto()1_Error: ").append(ex));
            try {
                if (transaction != null) {
                    transaction.rollback();
                }
                mensajeResultado.setNoError(ControlErroresHibernate.buscaNoErrorPorExcepcion(ex.getClass()));
                mensajeResultado.setError(ex.getLocalizedMessage());
            } catch (HibernateException exc) {  //cuando marca error al hacer rollback pro ejemplo se cae conexion servidor
                mensajeResultado.setNoError(ControlErroresHibernate.buscaNoErrorPorExcepcion(exc.getClass()));
                mensajeResultado.setError(exc.getLocalizedMessage());
            }
            mensajeResultado.setResultado(null);
        }
        return mensajeResultado;
    }

